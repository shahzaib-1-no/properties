    // Toggle the "active" class
    this.classList.toggle("active");

    let content = this.nextElementSibling;
    
    if (this.classList.contains("active")) {
      // If the accordion is open, set the max height
      content.style.maxHeight = content.scrollHeight + "px";
    } else {
      // If the accordion is closed, reset the max height
      content.style.maxHeight = null;
    }

    // Close other accordions
    accordionBtns.forEach((otherAccordion, i) => {
      if (otherAccordion !== accordion) {
        otherAccordion.classList.remove("active");
        otherAccordion.nextElementSibling.style.maxHeight = null;
      }
    });
  };

  // Activate the first accordion by default
